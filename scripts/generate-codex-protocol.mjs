#!/usr/bin/env node

import { existsSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const testedRange = ">=0.142.0 <0.143.0";
const min = [0, 142, 0];
const maxExclusive = [0, 143, 0];

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outDir = resolve(root, "src-tauri/protocol/codex-generated");
const prettierBin = resolve(root, "node_modules/.bin/prettier");

function run(command, args, options = {}) {
  const result = spawnSync(command, args, {
    cwd: root,
    encoding: "utf8",
    stdio: options.capture ? "pipe" : "inherit",
  });
  if (result.status !== 0) {
    const detail = result.stderr || result.stdout || "";
    throw new Error(`${command} ${args.join(" ")} failed.\n${detail.trim()}`);
  }
  return result.stdout?.trim() ?? "";
}

function parseVersion(versionText) {
  const token = versionText.split(/\s+/).find((part) => /^\d+\.\d+\.\d+/.test(part));
  if (!token) return null;
  const [major, minor, patch] = token.split(".").map((part) => Number.parseInt(part, 10));
  if ([major, minor, patch].some((part) => Number.isNaN(part))) return null;
  return [major, minor, patch];
}

function compareVersion(left, right) {
  for (let index = 0; index < 3; index += 1) {
    if (left[index] < right[index]) return -1;
    if (left[index] > right[index]) return 1;
  }
  return 0;
}

const versionText = run("codex", ["--version"], { capture: true });
const parsedVersion = parseVersion(versionText);
if (!parsedVersion) {
  throw new Error(`Could not parse Codex CLI version from: ${versionText}`);
}
if (compareVersion(parsedVersion, min) < 0 || compareVersion(parsedVersion, maxExclusive) >= 0) {
  console.warn(
    `Warning: Codex CLI ${versionText} is outside the tested OpenMindSteed app-server range ${testedRange}.`,
  );
}

rmSync(outDir, { force: true, recursive: true });
mkdirSync(outDir, { recursive: true });

const args = ["app-server", "generate-ts", "--experimental", "--out", outDir];
if (existsSync(prettierBin)) {
  args.push("--prettier", prettierBin);
}

run("codex", args);

writeFileSync(
  resolve(outDir, "README.md"),
  [
    "# Codex App-Server Protocol",
    "",
    "Generated by `pnpm run codex:protocol`.",
    "",
    `- Codex CLI: \`${versionText}\``,
    `- Tested OpenMindSteed range: \`${testedRange}\``,
    "- Experimental protocol fields are included.",
    "",
    "This directory is ignored by git because the protocol is version-specific. Regenerate it when updating the tested Codex CLI range or debugging app-server compatibility.",
    "",
  ].join("\n"),
);

console.log(`Generated Codex app-server protocol types in ${outDir}`);
